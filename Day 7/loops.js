// loops are those which is used to repeat the block of code until the given condition is true


// for loop
// we generally use for loop when numbers of iterations are fixed

for(let i=1;i<=10;i++){
    console.log(i);
}

// while loop
// we generally use while loop when the numbers of iterations are unknown

console.log("\n");

let i=1;
while(i<=10){
    console.log(i);
    i+=1;
}